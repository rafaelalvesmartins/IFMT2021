
public class Calculadora {
	
	double soma(double operando1, double operando2) {
		return (operando1 + operando2);
	}
	
	double subtracao(double operando1, double operando2) {
		return (operando1 - operando2);
	}
	
	double multiplicacao(double operando1, double operando2) {
		return (operando1 * operando2);
	}
	
	double divicao(double operando1, double operando2) {
		return (operando1 / operando2);
	}

}
